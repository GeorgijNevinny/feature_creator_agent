"""Системное сообщение и пользовательский шаблон для ответа LLM строго в JSON."""

from __future__ import annotations

# Описание целевого JSON — дублируется в пользовательском шаблоне для режима json_object.
JSON_OUTPUT_SPEC = """
Верни один JSON-объект (без Markdown, без пояснений до или после) со структурой:

{
  "dataset_summary": {
    "domain": "строка: предполагаемый домен/контекст данных",
    "what_is_measured": "строка: что именно отражают строки и столбцы",
    "possible_ml_task_types": ["одно или несколько из: regression, classification, clustering, time_series, unknown"],
    "possible_ml_task_comment": "строка: почему такие типы задач уместны или почему unknown",
    "uncertainty_note": "строка: явно укажи, если по профилю мало информации (мало строк, только агрегаты, обрезанные столбцы) — ты не уверен; иначе кратко «уверенность приемлемая» или аналог"
  },
  "column_roles": [
    {
      "column": "имя столбца ТОЛЬКО из поля columns[].name входного профиля",
      "roles": ["одно или несколько из: id, timestamp, target_candidate, feature, leakage_risk"],
      "rationale": "строка: почему такие роли"
    }
  ],
  "feature_ideas": [
    {
      "name": "строка: короткое имя признака",
      "formula_or_description": "строка: формула или словесное описание, используя только известные из профиля столбцы",
      "rationale": "строка",
      "applies_to_models": ["tabular", "nn"],
      "implementation_notes": "строка: нюансы типов, пропусков, масштаба — без выдуманных столбцов"
    }
  ]
}

Требования:
- В корне JSON обязаны быть ровно эти три ключа: "dataset_summary", "column_roles", "feature_ideas". Даже если идей признаков нет — верни "feature_ideas": [] (пустой массив), ключ не опускай.
- Используй ТОЛЬКО столбцы, перечисленные во входном профиле (columns[].name). Не выдумывай новые имена колонок.
- Если роль столбца неочевидна — ставь feature или сузь до id/timestamp по dtype и sample_values; target_candidate и leakage_risk — только при явных сигналах в профиле (имена, доли классов, утечки времени и т.д.), иначе осторожно помечай uncertainty в rationale.
- Язык значений строк (domain, rationale, notes): русский.
- applies_to_models: для каждой идеи укажи подмножество ["tabular","nn"] (можно оба).

ЖЁСТКИЙ КОНТРАКТ ВЫВОДА (соблюдай буквально):
- Один корень: только JSON-объект {...}, не массив, не обёртка в строку, не NDJSON.
- Первый значимый символ ответа — «{», последний замыкающий «}»; без преамбулы («Конечно…») и без постскриптума.
- Запрещено: Markdown, кодовые ограждения ```, HTML, комментарии // или /* */, незакрытые кавычки.
- На верхнем уровне только три ключа: "dataset_summary", "column_roles", "feature_ideas" — без других полей верхнего уровня.
- Все строки в JSON в двойных кавычках; числа и булевы значения — в синтаксисе JSON.
"""


SYSTEM_DATA_ANALYST = f"""Ты аналитик табличных данных. Тебе передают только агрегированный JSON-профиль датасета (без полных строк таблицы).

Твоя задача — заполнить ответ строго по схеме ниже. Не обучай модели и не генерируй исполняемый код.

КРИТИЧНО:
- Ответ — только валидный JSON-объект, без текста до/после, без блоков ```.
- Не придумывай столбцы: в column_roles и в formula_or_description / rationale признаков опирайся исключительно на имена из профиля.
- Если профиля недостаточно для уверенных выводов — отрази это в dataset_summary.uncertainty_note и в rationale.
- Режим ответа: структурированный JSON; никакого свободного текста вне объекта.

Схема ответа:
{JSON_OUTPUT_SPEC}
"""


def build_user_prompt(profile_json: str) -> str:
    """Пользовательское сообщение: профиль + напоминание о формате."""
    return f"""Профиль датасета (JSON) — единственный источник имён столбцов и агрегатов:

{profile_json}

Верни один JSON-объект строго по схеме из системного сообщения и по «ЖЁСТКОМУ КОНТРАКТУ ВЫВОДА» там же.
Обязательно включи ключ "feature_ideas" (массив; допустим пустой []). На верхнем уровне не добавляй других ключей кроме dataset_summary, column_roles, feature_ideas.
Каждое значение поля "column" в column_roles должно в точности совпадать с одним из columns[].name этого профиля. Не добавляй столбцы, которых нет в профиле."""
